// Export all stadiums combined
export const ALL_MILB_STADIUMS = [
  ...AAA_STADIUMS,
  ...AA_STADIUMS,
  ...HIGH_A_STADIUMS,
  ...LOW_A_STADIUMS
];

// Helper function to get stadiums by level
export function getMiLBStadiumsByLevel(level: MiLBStadium['level']): MiLBStadium[] {
  switch (level) {
    case 'AAA':
      return AAA_STADIUMS;
    case 'AA':
      return AA_STADIUMS;
    case 'A+':
      return HIGH_A_STADIUMS;
    case 'A':
      return LOW_A_STADIUMS;
    default:
      return [];
  }
}

// Helper function to get stadium by ID
export function getMiLBStadiumById(id: string): MiLBStadium | undefined {
  return ALL_MILB_STADIUMS.find(stadium => stadium.id === id);
}

// Helper function to get stadiums by parent organization
export function getMiLBStadiumsByParentOrg(parentOrg: string): MiLBStadium[] {
  return ALL_MILB_STADIUMS.filter(stadium => stadium.parentOrg === parentOrg);
}